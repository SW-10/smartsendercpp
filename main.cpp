#include <iostream>
#include "config/Configuration.h"
//using namespace config4cpp;

int main() {
    config4cpp::Configuration *  cfg = config4cpp::Configuration::create();
    const char *     scope = "";
    const char *     configFile = "moby.cfg";
    const char *     inputFile;
    cfg->parse(configFile);
    inputFile = cfg->lookupString(scope, "inputfile");
    std::cout << "Hello, World!" << inputFile << std::endl;
    return 0;
}
